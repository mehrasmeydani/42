#include <unistd.h>

void	ft_print_comb2(void)
{
	int		a[2];
	char	b[4];

	a[0] = 0;
	while (a[0] <= 98)
	{
		a[1] = a[0] +1;
		while (a[1] <= 99)
		{
			b[0] = a[0] / 10 + '0';
			b[1] = a[0] % 10 + '0';
			b[2] = a[1] / 10 + '0';
			b[3] = a[1] % 10 + '0';
			write(1, &b[0], 1);
			write(1, &b[1], 1);
			write(1, " ", 1);
			write(1, &b[2], 1);
			write(1, &b[3], 1);
			if (a[0] != 98)
				write(1, ", ", 2);
			a[1]++;
		}
		a[0]++;
	}
}
