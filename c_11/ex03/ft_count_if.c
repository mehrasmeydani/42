int	ft_count_if(char **tab, int length, int (*f)(char*))
{
	int	i;
	int	cnt;

	i = -1;
	cnt = 0;
	while (++i < length)
		if ((*f)(tab[i]))
			cnt++;
	return (cnt);
}
