#include <unistd.h>
#include <stdio.h>

void	decimal_heximal(char *p, unsigned char n)
{
	unsigned int	remain;
	int				i;
	int				cnt;

	i = 0;
	cnt = 2;
	while (n != 0 || cnt != 0)
	{
		remain = n % 16;
		if (remain < 10)
		{
			p[i++] = (remain % 10) + '0';
		}
		else
		{
			p[i++] = (remain % 10) + 'a';
		}
		n = n / 16;
		cnt --;
	}
	write(1, "\\", 1);
	write(1, p + 1, 1);
	write(1, p, 1);
}

int	ft_char_is_printable(char *str)
{
	if (!(*str >= 32 && *str <= 126))
		return (0);
	return (1);
}

void	ft_putstr_non_printable(char *str)
{
	char	hex[2];

	while (*str != '\0')
	{
		if (ft_char_is_printable(str))
		{
			write(1, str, 1);
		}
		else
		{
			decimal_heximal(hex, *str);
		}
		str++;
	}
}
