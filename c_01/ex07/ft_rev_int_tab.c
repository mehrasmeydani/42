void	ft_rev_int_tab(int *tab, int size)
{
	int	tmp;
	int	a;
	int	b;

	a = 0;
	b = size - 1;
	while (a < b)
	{
		tmp = tab[a];
		tab[a] = tab[b];
		tab[b] = tmp;
		a++;
		b--;
	}
}
