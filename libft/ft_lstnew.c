#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*out;

	out = (t_list *)malloc(sizeof(*out));
	if (!out)
		return (NULL);
	out->content = content;
	out->next = NULL;
	return (out);
}
